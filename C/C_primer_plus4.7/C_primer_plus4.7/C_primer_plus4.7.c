#include<stdio.h>
#define PAGES 195913295959
int main(void)
{
	printf("*%lld*\n", PAGES);
	printf("*%2lld*\n", PAGES);
	printf("*%13lld*\n", PAGES);
	printf("*%-13lld*", PAGES);//标记-代表从字段左侧开始打印

	return 0;
}
